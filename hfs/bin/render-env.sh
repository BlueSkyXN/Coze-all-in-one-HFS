#!/usr/bin/env bash
set -euo pipefail

ENV_FILE="${COZE_ENV_FILE:-/app/.env}"
DATA_DIR="${DATA_DIR:-/data/coze}"
GENERATED_ENV_FILE="${COZE_GENERATED_ENV_FILE:-$DATA_DIR/generated-secrets.env}"
SPACE_HOST_VALUE="${SPACE_HOST:-}"

if [ -n "${COZE_PUBLIC_URL:-}" ]; then
  PUBLIC_URL="${COZE_PUBLIC_URL}"
elif [ -n "$SPACE_HOST_VALUE" ]; then
  PUBLIC_URL="https://${SPACE_HOST_VALUE}"
else
  PUBLIC_URL="http://localhost:7860"
fi

secret_value() {
  /usr/bin/python3 - <<'PY'
import secrets
print(secrets.token_urlsafe(32))
PY
}

ensure_generated_secret() {
  local key="$1"
  local value=""
  if [ -n "${!key-}" ]; then
    return 0
  fi
  if grep -q "^export ${key}=" "$GENERATED_ENV_FILE" 2>/dev/null; then
    return 0
  fi
  value="$(secret_value)"
  printf 'export %s=%q\n' "$key" "$value" >> "$GENERATED_ENV_FILE"
}

SECRET_KEYS=(
  MYSQL_ROOT_PASSWORD
  MYSQL_PASSWORD
  MINIO_ROOT_PASSWORD
  PLUGIN_AES_AUTH_SECRET
  PLUGIN_AES_STATE_SECRET
  PLUGIN_AES_OAUTH_TOKEN_SECRET
)

USER_OVERRIDE_KEYS=()
USER_OVERRIDE_VALUES=()
for key in "${SECRET_KEYS[@]}"; do
  if [ -n "${!key-}" ]; then
    USER_OVERRIDE_KEYS+=("$key")
    USER_OVERRIDE_VALUES+=("${!key}")
  fi
done

restore_user_overrides() {
  local index
  for index in "${!USER_OVERRIDE_KEYS[@]}"; do
    export "${USER_OVERRIDE_KEYS[$index]}=${USER_OVERRIDE_VALUES[$index]}"
  done
}

mkdir -p "$DATA_DIR"
touch "$GENERATED_ENV_FILE"
chmod 600 "$GENERATED_ENV_FILE"

# shellcheck disable=SC1090
source "$GENERATED_ENV_FILE"
restore_user_overrides

for key in "${SECRET_KEYS[@]}"; do
  ensure_generated_secret "$key"
done

# shellcheck disable=SC1090
source "$GENERATED_ENV_FILE"
restore_user_overrides

q() {
  # Shell-quote for .env export lines.
  printf "%q" "$1"
}

emit() {
  local key="$1"
  local default="$2"
  local value="${!key-}"
  if [ -z "${value}" ]; then
    value="$default"
  fi
  printf 'export %s=%s\n' "$key" "$(q "$value")" >> "$ENV_FILE"
}

