#!/usr/bin/env bash
set -euo pipefail

IMAGE_NAME="${IMAGE_NAME:-coze-studio-hfs-poc:local}"
COZE_SERVER_TAG="${COZE_SERVER_TAG:-0.5.1@sha256:bacce3aa5960a2f18362eac93317e42a8c0dbd125a44ec47519f12a8a27c7744}"
COZE_WEB_TAG="${COZE_WEB_TAG:-0.5.1@sha256:a137a16ab75b871b08911ca87359fc8981b225b63b94cf3e0979069fbd862aea}"
COZE_GIT_REF="${COZE_GIT_REF:-v0.5.1}"
ELASTICSEARCH_IMAGE="${ELASTICSEARCH_IMAGE:-bitnamilegacy/elasticsearch:8.18.0@sha256:4a7d14222c876a87c1ddd38e1128d8e42df80071b09ec54db5c32586c9cf5a38}"
ETCD_IMAGE="${ETCD_IMAGE:-bitnamilegacy/etcd:3.5@sha256:1b9977cf4cce7546873e0ee50e684c38a38a4e7a27d22086fbd2b8a1b44a69d0}"
MILVUS_IMAGE="${MILVUS_IMAGE:-milvusdb/milvus:v2.5.10@sha256:02e1d60d71ab60f435c60076f4fed2abe59602ecd5e18dcfe229c8c558c4379d}"
DENO_VERSION="${DENO_VERSION:-2.4.5}"
ATLAS_VERSION="${ATLAS_VERSION:-v1.2.0}"
MINIO_VERSION="${MINIO_VERSION:-RELEASE.2025-09-07T16-13-09Z}"
MC_VERSION="${MC_VERSION:-RELEASE.2025-08-13T08-35-41Z}"

build_args=(
  --build-arg COZE_SERVER_TAG="$COZE_SERVER_TAG"
  --build-arg COZE_WEB_TAG="$COZE_WEB_TAG"
  --build-arg COZE_GIT_REF="$COZE_GIT_REF"
  --build-arg ELASTICSEARCH_IMAGE="$ELASTICSEARCH_IMAGE"
  --build-arg ETCD_IMAGE="$ETCD_IMAGE"
  --build-arg MILVUS_IMAGE="$MILVUS_IMAGE"
  --build-arg DENO_VERSION="$DENO_VERSION"
  --build-arg ATLAS_VERSION="$ATLAS_VERSION"
  --build-arg MINIO_VERSION="$MINIO_VERSION"
  --build-arg MC_VERSION="$MC_VERSION"
)

for key in \
  DENO_SHA256_AMD64 \
  DENO_SHA256_ARM64 \
  ATLAS_SHA256_AMD64 \
  ATLAS_SHA256_ARM64 \
  MINIO_SHA256_AMD64 \
  MINIO_SHA256_ARM64 \
  MC_SHA256_AMD64 \
  MC_SHA256_ARM64; do
  if [ -n "${!key:-}" ]; then
    build_args+=(--build-arg "$key=${!key}")
  fi
done

docker build \
  "${build_args[@]}" \
  -t "$IMAGE_NAME" \
  .
